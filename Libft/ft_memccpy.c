#include "libft.h"

void	*ft_memccpy(void *dest, void *src, int c, size_t n)
{
	char	*d;
	char	*s;

	d = dest;
	s = src;
	while(n--)
	{
		if ((*d++ = *s++) == c)
			return (d);
	}
	return (0);
}
